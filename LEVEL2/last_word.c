#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
			i++;
		i--;
		while (i >= 0 && (argv[1][i] == 9 || argv[1][i] == 32))
			i--;
		while (i >= 0 && (argv[1][i] != 9 && argv[1][i] != 32))
			i--;
		i++;
		while (argv[1][i] != '\0' && (argv[1][i] != 9 && argv[1][i] != 32))
		{
			write(1, &argv[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}

char	*ft_strpbrk(const char *s1, const char *s2)
{
	int	i;
	int	b;

	i = 0;
	while (s1[i] != '\0')
	{
		b = 0;
		while (s2[b] != '\0')
		{
			if (s1[i] == s2[b])
				return ((char *)(s1 + i));
			b++;
		}
		i++;
	}
	return (NULL);
}

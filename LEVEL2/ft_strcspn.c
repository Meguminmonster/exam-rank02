size_t	ft_strcspn(const char *s, const char *reject)
{
	size_t	i;
	size_t	b;

	i = 0;
	while (s[i] != '\0')
	{
		b = 0;
		while (reject[b] != '\0')
		{
			if (s[i] == reject[b])
				return (i);
			b++;
		}
		i++;
	}
	return (i);
}

#include <unistd.h>

int	main(int argc, char *argv)
{
	int	i;
	int	b;

	i = 0;
	b = 0;
	if (argc == 3)
	{
		while (argv[2][b] && argv[1][i])
		{
			if (argv[2][b] == argv[1][i])
				i++;
			b++;
		}
		if (argv[1][i] == '\0')
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
	write(1, "\n", 1);
	return (0);
}
